import { execSync } from "child_process";
import fs from "fs";
import path from "path";

/**
 * 📝 NEXO AUTO-CHANGELOG ENGINE
 * Generates a professional CHANGELOG.md based on conventional commit history.
 */

const CHANGELOG_PATH = path.join(process.cwd(), "CHANGELOG.md");

function generateChangelog() {
    console.log("📝 Orchestrating Automated Changelog...");

    try {
        // Get the last 20 commits
        const gitLog = execSync('git log -n 20 --pretty=format:"* %s (%h)"').toString();
        
        const lines = gitLog.split("\n");
        const categories = {
            feat: [],
            fix: [],
            docs: [],
            refactor: [],
            other: []
        };

        lines.forEach(line => {
            const lower = line.toLowerCase();
            if (lower.includes("feat:")) categories.feat.push(line);
            else if (lower.includes("fix:")) categories.fix.push(line);
            else if (lower.includes("docs:")) categories.docs.push(line);
            else if (lower.includes("refactor:")) categories.refactor.push(line);
            else categories.other.push(line);
        });

        let markdown = `# 📜 PROJECT CHRONICLE (CHANGELOG)\n\n`;
        markdown += `> [!NOTE]\n> **Automated Version Tracking**\n> This document is synchronized with the repository's commit history.\n\n`;

        if (categories.feat.length) markdown += `### 🚀 New Capabilities\n${categories.feat.join("\n")}\n\n`;
        if (categories.fix.length) markdown += `### 🛠️ Stability Fixes\n${categories.fix.join("\n")}\n\n`;
        if (categories.docs.length) markdown += `### 📚 Documentation Intelligence\n${categories.docs.join("\n")}\n\n`;
        if (categories.refactor.length) markdown += `### 🏗️ Architectural Refinements\n${categories.refactor.join("\n")}\n\n`;
        if (categories.other.length) markdown += `### 📦 Miscellaneous Updates\n${categories.other.join("\n")}\n\n`;

        markdown += `---\n*Generated by Nexo-Release-Engine | ${new Date().toLocaleDateString()}*`;

        fs.writeFileSync(CHANGELOG_PATH, markdown);
        console.log("✨ CHANGELOG.md synchronized successfully.");
        
    } catch (error) {
        console.error("❌ Changelog sync failed (Check if Git is initialized):", error.message);
    }
}

generateChangelog();
